function r = rho3(x)

a(1) = 0.34003812123694735361;
b(1) = (2304*a(1)^3 + 656*a(1)^2 - 421*a(1) - 28)/...
    (7680*a(1)^2 - 1680*a(1) - 330);
a(2) = a(1)*(b(1) - 1/4);
b(2) = (768*a(1)*b(1) - 120*b(1) - 384*a(1)^2 + 8*a(1) + 7)/...
    (4680*a(1) - 1152);
a(3) = (a(1)*(192*b(2) - 48*b(1) + 16*a(1) + 5))/192;

r = (a(1)*x.^2 + a(2)*x.^4 + a(3)*x.^6)./...
    (1 + b(1)*x.^2 + b(2)*x.^4 + a(3)*x.^6);

end