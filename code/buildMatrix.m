function [d1, d2] = buildMatrix(m, h)

% build matrix for 4-th order equispaced finite difference

d1 = spdiags(ones(m,1)*[-1/12, 2/3, 0, -2/3, 1/12]/h, -2:2, m,m);
d2 = spdiags(ones(m,1)*[-1/12, 4/3, -5/2, 4/3, -1/12]/(h^2), -2:2, m,m);

d1(1,1:5) = [-25/12, 4, -3, 4/3, -1/4]/h; 
d1(2,1:5) = [-1/4, -5/6, 3/2, -1/2, 1/12]/h; 
d1(m-1, m-4:m) = [-1/12,1/2,-3/2,5/6,1/4]/h; 
d1(m,m-4:m) = [1/4,-4/3,3,-4,25/12]/h;

d2(2,1:6) = [5/6,-5/4,-1/3,7/6,-1/2,1/12]/(h^2);
d2(m-1,m-5:m) = [1/12,-1/2,7/6,-1/3,-5/4,5/6]/(h^2);
d2(1,1:6) = [45 -154 214 -156 61 -10]/(12*h^2);
d2(m,m-5:m) = [-10 61 -156 214 -154 45]/(12*h^2);

end